package patata;

public class ClasePrimera {
	private int numUno;
	private int numTres;
	public ClasePrimera(int numUno, int numTres) {
		super();
		this.numUno = numUno;
		this.numTres = numTres;
	}
	public int getNumUno() {
		return numUno;
	}
	public void setNumUno(int numUno) {
		this.numUno = numUno;
	}
	public int getNumTres() {
		return numTres;
	}
	public void setNumTres(int numTres) {
		this.numTres = numTres;
	}
	
}
